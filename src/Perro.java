public class Perro {

    private String pelo;
    private String color;
    private int chip;

    public String getPelo(){
        return pelo;
    }

    public void setPelo(String pelo){
        this.pelo = pelo;
    }


    public String getColor(){
        return color;
    }


    public void setColor(String color){
        this.color = color;
    }


    public int getChip(){
        return chip;
    }


    public void setChip(int chip){
        this.chip = chip;
    }
}
